export function colorFromName(input: string) {
  let hash = 2166136261;
  for (let i = 0; i < input.length; i++) {
    hash ^= input.charCodeAt(i);
    hash = Math.imul(hash, 16777619);
  }
  return FOOD_COLORS[(hash >>> 0) % FOOD_COLORS.length];
}
